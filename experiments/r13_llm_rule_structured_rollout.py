R13_SCHEMA_VERSION = "r13-llm-rule-structured-rollout-v1"
R13_CLAIM_BOUNDARY = (
    "R13 structured rollout artifact. It demonstrates that LLM-generated "
    "segment/persona behavior rules can be compiled into a structured rollout "
    "over 10k+ synthetic individuals without per-individual LLM calls. It is "
    "not customer field validation, not runtime default authorization, and not "
    "a claim that every synthetic individual was individually generated by an LLM."
)

RuleCompletionFn = Callable[[str, str], str]


def build_r13_llm_rule_structured_rollout(
    *,
    artifact_id: str,
    run_id: str,
    case_template: dict[str, Any],
    synthetic_individual_count: int = 10_000,
    rule_completion_fn: RuleCompletionFn | None = None,
    rule_source: str | None = None,
    provider: str = "offline",
    model: str = "fixture-rule-generator",
    base_url: str | None = None,
    seed: int = 42,
) -> dict[str, Any]:
    artifact_id = non_empty_string(artifact_id, field="artifact_id")
    run_id = non_empty_string(run_id, field="run_id")
    if synthetic_individual_count < 1:
        raise ValueError("synthetic_individual_count must be positive")
    if not isinstance(case_template, dict):
        raise ValueError("case_template must be an object")

    segments = _prior_segments(case_template)
    system_prompt, user_prompt = _build_rule_prompt(case_template, segments)
    llm_call_count = 0
    if rule_completion_fn is None:
        raw_rule_text = _offline_fixture_rule_response(case_template)
        resolved_rule_source = rule_source or "offline_fixture_llm_shape"
    else:
        raw_rule_text = rule_completion_fn(system_prompt, user_prompt)
        llm_call_count = 1
        resolved_rule_source = rule_source or "llm_completion_fn"

    behavior_rules = _parse_and_validate_rules(raw_rule_text, segments)
    segment_allocations = _allocate_segments(
        segments=segments,
        synthetic_individual_count=synthetic_individual_count,
    )
    rollout = _run_structured_rollout(
        case_template=case_template,
        segments=segments,
        behavior_rules=behavior_rules,
        segment_allocations=segment_allocations,
        synthetic_individual_count=synthetic_individual_count,
        seed=seed,
    )
    segment_count = len(segments)
    artifact = {
        "schema_version": R13_SCHEMA_VERSION,
        "artifact_id": artifact_id,
        "run_id": run_id,
        "status": "r13_llm_rule_structured_rollout_ready_guarded",
        "case_id": case_template.get("case_id"),
        "case_type": case_template.get("case_type"),
        "scenario": case_template.get("scenario", {}),
        "synthetic_population": {
            "individual_count": synthetic_individual_count,
            "segment_count": segment_count,
            "segment_allocations": segment_allocations,
            "generation_method": "weighted_prior_expansion_structured_rollout",
        },
        "llm_rule_generation": {
            "rule_source": resolved_rule_source,
            "provider": provider,
            "model": model,
            "base_url": base_url,
            "llm_call_count": llm_call_count,
            "max_allowed_llm_calls": segment_count,
            "per_individual_llm_calls": False,
            "prompt_contract": {
                "requires_json_rules": True,
                "required_rule_fields": [
                    "segment_id",
                    "persona_rule",
                    "reject_delta",
                    "volatility",
                    "mechanisms",
                ],
            },
        },
        "behavior_rules": behavior_rules,
        "structured_rollout": rollout,
        "acceptance_gates": {
            "llm_rule_generation_present": bool(behavior_rules),
            "synthetic_population_10k_met": synthetic_individual_count >= 10_000,
            "llm_calls_not_per_individual": llm_call_count < synthetic_individual_count,
            "all_segments_have_rules": len(behavior_rules) == segment_count,
            "structured_rollout_metrics_present": _rollout_metrics_present(rollout),
            "field_outcome_validated": False,
            "product_default_allowed": False,
            "runtime_default_allowed": False,
        },
        "source_refs": [
            f"case_template:{case_template.get('case_id')}",
            "r13_llm_rule_structured_rollout",
        ],
        "claim_boundary": R13_CLAIM_BOUNDARY,
        "risk_flags": [
            "not_field_validated",
            "not_runtime_default",
            "synthetic_rollout_not_customer_outcome",
            "llm_rules_are_segment_level_not_per_individual_generation",
        ],
    }
    assert_strict_json(artifact)
    return artifact


def write_r13_llm_rule_structured_rollout(
    output: str | Path,
    **kwargs: Any,
) -> Path:
    return write_json_artifact(output, build_r13_llm_rule_structured_rollout(**kwargs))


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--artifact-id", default="r13-llm-rule-structured-rollout-current-001")
    parser.add_argument("--run-id", default="r13-llm-rule-structured-rollout-current-001")
    parser.add_argument("--case-id", default="generic-price-change")
    parser.add_argument("--synthetic-individual-count", type=int, default=10_000)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument(
        "--output",
        default=(
            "experiments/results/r13_llm_rule_structured_rollout/"
            "r13-llm-rule-structured-rollout-current-001.json"
        ),
    )
    parser.add_argument("--execute-llm", action="store_true")
    parser.add_argument("--provider", default="openai")
    parser.add_argument("--model", default="deepseek-v4-flash")
    parser.add_argument("--base-url", default="https://api.deepseek.com")
    args = parser.parse_args()

    case_template = _case_template_by_id(args.case_id)
    completion_fn: RuleCompletionFn | None = None
    rule_source = "offline_fixture_llm_shape"
    provider = "offline"
    model = "fixture-rule-generator"
    base_url = None
    if args.execute_llm:
        completion_fn = _build_live_rule_completion_fn(
            provider=args.provider,
            model=args.model,
            base_url=args.base_url,
        )
        rule_source = "live_llm_provider"
        provider = args.provider
        model = args.model
        base_url = args.base_url

    output = write_r13_llm_rule_structured_rollout(
        args.output,
        artifact_id=args.artifact_id,
        run_id=args.run_id,
        case_template=case_template,
        synthetic_individual_count=args.synthetic_individual_count,
        rule_completion_fn=completion_fn,
        rule_source=rule_source,
        provider=provider,
        model=model,
        base_url=base_url,
        seed=args.seed,
    )
    artifact = json.loads(Path(output).read_text())
    print(
        json.dumps(
            {
                "output": str(output),
                "status": artifact["status"],
                "synthetic_individual_count": artifact["synthetic_population"][
                    "individual_count"
                ],
                "llm_call_count": artifact["llm_rule_generation"]["llm_call_count"],
                "rule_source": artifact["llm_rule_generation"]["rule_source"],
            },
            sort_keys=True,
            allow_nan=False,
        )
    )
    return 0


def _prior_segments(case_template: dict[str, Any]) -> list[dict[str, Any]]:
    segments = case_template.get("prior_segments")
    if not isinstance(segments, list) or not segments:
        raise ValueError("case_template.prior_segments must be a non-empty list")
    total_weight = sum(float(segment.get("weight", 0)) for segment in segments)
    if not math.isclose(total_weight, 1.0, abs_tol=1e-6):
        raise ValueError("prior segment weights must sum to 1.0")
    return segments


def _build_rule_prompt(
    case_template: dict[str, Any],
    segments: list[dict[str, Any]],
) -> tuple[str, str]:
    system = (
        "You generate segment/persona behavior rules for a crowd simulation. "
        "Return strict JSON only."
    )
    user = {
        "task": (
            "Generate one behavior rule per segment. Each rule must include "
            "segment_id, persona_rule, reject_delta, volatility, and mechanisms."
        ),
        "case_id": case_template.get("case_id"),
        "scenario": case_template.get("scenario", {}),
        "segments": [
            {
                "segment_id": segment["segment_id"],
                "weight": segment["weight"],
                "static_traits": segment.get("static_traits", {}),
                "static_response_prior": segment.get("static_response_prior", {}),
            }
            for segment in segments
        ],
    }
    return system, json.dumps(user, ensure_ascii=False, sort_keys=True)


def _offline_fixture_rule_response(case_template: dict[str, Any]) -> str:
    shifts = {
        item["segment_id"]: item
        for item in case_template.get("interaction_profile", {}).get(
            "segment_shifts", []
        )
    }
    rules = []
    for segment in case_template.get("prior_segments", []):
        segment_id = segment["segment_id"]
        shift = shifts.get(segment_id, {})
        delta = float(shift.get("delta_distribution", {}).get("reject", 0.02))
        mechanisms = shift.get("mechanisms", ["prior_adjusted_response"])
        traits = ", ".join(
            f"{key}={value}" for key, value in segment.get("static_traits", {}).items()
        )
        rules.append(
            {
                "segment_id": segment_id,
                "persona_rule": (
                    f"Segment {segment_id} follows static traits ({traits}) and "
                    "adjusts response through scenario mechanisms."
                ),
                "reject_delta": delta,
                "volatility": float(segment.get("uncertainty", 0.1)) * 0.35,
                "mechanisms": mechanisms,
            }
        )
    return json.dumps({"rules": rules}, ensure_ascii=False)


def _parse_and_validate_rules(
    raw_rule_text: str,
    segments: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    if not isinstance(raw_rule_text, str) or not raw_rule_text.strip():
        raise ValueError("LLM rule response must be a non-empty string")
    payload = _loads_rule_json(raw_rule_text)
    rules = payload.get("rules")
    if not isinstance(rules, list) or not rules:
        raise ValueError("LLM rule response must contain non-empty rules list")

    known_segments = {segment["segment_id"] for segment in segments}
    validated: list[dict[str, Any]] = []
    seen_segments: set[str] = set()
    for index, rule in enumerate(rules):
        if not isinstance(rule, dict):
            raise ValueError(f"rules[{index}] must be an object")
        segment_id = non_empty_string(rule.get("segment_id"), field=f"rules[{index}].segment_id")
        if segment_id not in known_segments:
            raise ValueError(f"unknown segment_id in rules[{index}]: {segment_id}")
        if segment_id in seen_segments:
            raise ValueError(f"duplicate segment_id in rules: {segment_id}")
        seen_segments.add(segment_id)
        reject_delta = _finite_bounded_float(
            rule.get("reject_delta"),
            field=f"rules[{index}].reject_delta",
            lower=-0.5,
            upper=0.5,
        )
        volatility = _finite_bounded_float(
            rule.get("volatility"),
            field=f"rules[{index}].volatility",
            lower=0.0,
            upper=0.5,
        )
        mechanisms = _mechanism_list(rule.get("mechanisms"), field=f"rules[{index}].mechanisms")
        validated.append(
            {
                "segment_id": segment_id,
                "persona_rule": non_empty_string(
                    rule.get("persona_rule"),
                    field=f"rules[{index}].persona_rule",
                ),
                "reject_delta": round(reject_delta, 6),
                "volatility": round(volatility, 6),
                "mechanisms": mechanisms,
            }
        )
    missing = sorted(known_segments - seen_segments)
    if missing:
        raise ValueError(f"missing segment rules: {missing}")
    return sorted(validated, key=lambda item: item["segment_id"])


def _loads_rule_json(raw_rule_text: str) -> dict[str, Any]:
    try:
        payload = json.loads(raw_rule_text)
    except json.JSONDecodeError:
        match = re.search(r"\{.*\}", raw_rule_text, flags=re.DOTALL)
        if not match:
            raise ValueError("LLM rule response is not JSON") from None
        try:
            payload = json.loads(match.group(0))
        except json.JSONDecodeError as exc:
            raise ValueError("LLM rule response is not valid JSON") from exc
    if not isinstance(payload, dict):
        raise ValueError("LLM rule response must be a JSON object")
    return payload


def _finite_bounded_float(
    value: Any,
    *,
    field: str,
    lower: float,
    upper: float,
) -> float:
    if isinstance(value, bool) or not isinstance(value, (int, float)):
        raise ValueError(f"{field} must be numeric")
    number = float(value)
    if not math.isfinite(number) or number < lower or number > upper:
        raise ValueError(f"{field} must be between {lower} and {upper}")
    return number


def _mechanism_list(value: Any, *, field: str) -> list[str]:
    if not isinstance(value, list) or not value:
        raise ValueError(f"{field} must be a non-empty list")
    return [non_empty_string(item, field=f"{field}[]") for item in value]


def _allocate_segments(
    *,
    segments: list[dict[str, Any]],
    synthetic_individual_count: int,
) -> list[dict[str, Any]]:
    exact = [
        (segment, float(segment["weight"]) * synthetic_individual_count)
        for segment in segments
    ]
    floors = [int(math.floor(value)) for _, value in exact]
    remainder = synthetic_individual_count - sum(floors)
    ranked_fractional = sorted(
        enumerate(exact),
        key=lambda item: item[1][1] - math.floor(item[1][1]),
        reverse=True,
    )
    counts = floors[:]
    for index, _ in ranked_fractional[:remainder]:
        counts[index] += 1
    return [
        {
            "segment_id": segment["segment_id"],
            "weight": float(segment["weight"]),
            "individual_count": counts[index],
        }
        for index, segment in enumerate(segments)
    ]


def _run_structured_rollout(
    *,
    case_template: dict[str, Any],
    segments: list[dict[str, Any]],
    behavior_rules: list[dict[str, Any]],
    segment_allocations: list[dict[str, Any]],
    synthetic_individual_count: int,
    seed: int,
) -> dict[str, Any]:
    rng = np.random.default_rng(seed)
    rules_by_segment = {rule["segment_id"]: rule for rule in behavior_rules}
    allocations = {item["segment_id"]: item["individual_count"] for item in segment_allocations}
    segment_results = []
    total_static_reject = 0.0
    total_interaction_reject_count = 0
    mechanism_weights: dict[str, int] = {}
    for segment in segments:
        segment_id = segment["segment_id"]
        count = allocations[segment_id]
        prior = segment["static_response_prior"]
        static_reject = float(prior["reject"])
        rule = rules_by_segment[segment_id]
        interaction_reject = _clamp(static_reject + rule["reject_delta"], 0.001, 0.999)
        reject_count = int(rng.binomial(count, interaction_reject))
        total_static_reject += static_reject * count
        total_interaction_reject_count += reject_count
        for mechanism in rule["mechanisms"]:
            mechanism_weights[mechanism] = mechanism_weights.get(mechanism, 0) + count
        segment_results.append(
            {
                "segment_id": segment_id,
                "individual_count": count,
                "static_reject_rate": round(static_reject, 6),
                "interaction_reject_probability": round(interaction_reject, 6),
                "simulated_reject_count": reject_count,
                "simulated_reject_rate": round(reject_count / count if count else 0.0, 6),
                "reject_rate_delta": round(interaction_reject - static_reject, 6),
                "mechanisms": rule["mechanisms"],
            }
        )

    static_reject_rate = total_static_reject / synthetic_individual_count
    interaction_reject_rate = total_interaction_reject_count / synthetic_individual_count
    rate_delta = interaction_reject_rate - static_reject_rate
    interval = _binomial_interval(interaction_reject_rate, synthetic_individual_count)
    top_risk_segments = sorted(
        segment_results,
        key=lambda item: (item["simulated_reject_rate"], item["reject_rate_delta"]),
        reverse=True,
    )
    abnormal_segments = [
        {
            "segment_id": item["segment_id"],
            "reject_rate_delta": item["reject_rate_delta"],
            "simulated_reject_rate": item["simulated_reject_rate"],
            "mechanisms": item["mechanisms"],
        }
        for item in top_risk_segments
        if item["reject_rate_delta"] >= 0.05 or item["simulated_reject_rate"] >= 0.5
    ]
    return {
        "scenario_id": case_template.get("scenario", {}).get("scenario_id"),
        "individuals_evaluated": synthetic_individual_count,
        "static_reject_rate": round(static_reject_rate, 6),
        "interaction_reject_rate": round(interaction_reject_rate, 6),
        "reject_rate_delta": round(rate_delta, 6),
        "trend_direction": _trend_direction(rate_delta),
        "risk_interval": interval,
        "segment_results": segment_results,
        "top_risk_segments": [
            {
                "segment_id": item["segment_id"],
                "simulated_reject_rate": item["simulated_reject_rate"],
                "reject_rate_delta": item["reject_rate_delta"],
                "individual_count": item["individual_count"],
            }
            for item in top_risk_segments[:3]
        ],
        "abnormal_segments": abnormal_segments,
        "risk_distribution": {
            "high_risk_segment_count": sum(
                1 for item in segment_results if item["simulated_reject_rate"] >= 0.5
            ),
            "high_risk_individual_count": sum(
                item["simulated_reject_count"]
                for item in segment_results
                if item["simulated_reject_rate"] >= 0.5
            ),
        },
        "mechanism_summary": {
            key: round(value / synthetic_individual_count, 6)
            for key, value in sorted(
                mechanism_weights.items(),
                key=lambda item: (-item[1], item[0]),
            )
        },
    }


def _binomial_interval(rate: float, count: int) -> dict[str, float]:
    standard_error = math.sqrt(max(rate * (1 - rate), 0.0) / count)
    margin = 1.96 * standard_error
    return {
        "method": "normal_approx_95pct_structured_rollout",
        "lower": round(_clamp(rate - margin, 0.0, 1.0), 6),
        "upper": round(_clamp(rate + margin, 0.0, 1.0), 6),
    }


def _trend_direction(rate_delta: float) -> str:
    if rate_delta >= 0.02:
        return "risk_up"
    if rate_delta <= -0.02:
        return "risk_down"
    return "roughly_stable"


def _rollout_metrics_present(rollout: dict[str, Any]) -> bool:
    required = [
        "trend_direction",
        "risk_interval",
        "risk_distribution",
        "top_risk_segments",
        "abnormal_segments",
        "mechanism_summary",
    ]
    return all(key in rollout for key in required)


def _clamp(value: float, lower: float, upper: float) -> float:
    return min(max(value, lower), upper)


def _case_template_by_id(case_id: str) -> dict[str, Any]:
    for template in R6_CASE_TEMPLATES:
        if template.get("case_id") == case_id:
            return template
    raise ValueError(f"unknown case_id: {case_id}")


def _build_live_rule_completion_fn(
    *,
    provider: str,
    model: str,
    base_url: str | None,
) -> RuleCompletionFn:
    from circe.llm_client import LLMClient, LLMClientConfig

    client = LLMClient(
        LLMClientConfig(
            provider=provider,
            model=model,
            base_url=base_url,
            max_tokens=1200,
            temperature=0.0,
        )
    )

    def completion(system: str, user: str) -> str:
        return client.chat(system=system, user=user).content

    return completion


